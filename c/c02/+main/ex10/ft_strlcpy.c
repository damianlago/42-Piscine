#include <unistd.h>
#include <stdio.h>

unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int i;

	i = 0;
	if (size > 0)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	i = 0;
	while (src[i] != '\0')
		i++;
	return (i);
}

int				main(void)
{
	char dest[8] = "fff";
	char src[] = "nnd";
	unsigned int size = 7;
	unsigned int i = ft_strlcpy(dest, src, size);
	printf("%d", i);
	printf("%s", dest);
	return (0);
}

