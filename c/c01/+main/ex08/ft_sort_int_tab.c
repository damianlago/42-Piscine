#include <unistd.h>
#include <stdio.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int temp;

	i = 0;
	while (i < size)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				temp = tab[i];
				tab[i] = tab[j];
				tab[j] = temp;
			}
			j++;
		}
		i++;
	}	
}

int		main(void)
{
	int tab[] = {80,20,10,40};
	int size = 4;
	ft_sort_int_tab(tab, size);
	for(int i = 0;i < size;i++)
		printf("%d\n", tab[i]);
	return (0);
}
