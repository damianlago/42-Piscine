#include <unistd.h>
#include <stdio.h>

void	ft_swap(int *x, int *y)
{
	int z;

	z = *x;
	*x = *y;
	*y = z;
}

int		main(void)
{
	int x = 5;
	int y = 10;
	ft_swap(&x, &y);
	printf("x: %d y: %d", x, y);
}
